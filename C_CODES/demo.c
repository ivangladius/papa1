#include <stdio.h>

int main (void){

    int n = 0;
    int sum = 0;
    while (n != 0) {
         scanf("%d", &i);
         if (i == 0) {
            continue;
         sum += i;
         n++;
          
         }
      
    }
    printf("The sum is: %d\n", sum);
    return 0;
}